def mediaAnual(faturamento):
    soma = 0
    tamanho = len(faturamento)
    for i in range(len(faturamento)):
        if faturamento[i] == 0:
            tamanho -= 1
        else:
            soma += faturamento[i]
    return soma/tamanho

